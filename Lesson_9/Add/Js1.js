//     Скріпт повинен працювати навіть якщо кількість блоків з заголовком та параграфом зміниться.

const getClassRules = document.getElementsByClassName("rules")
for (const time of getClassRules) {
    const ulElement = document.createElement("ul")
    document.getElementById("content").appendChild(ulElement)
    const element = time.children
    for (const elementElement of element) {
        if (elementElement.localName === "h2"){
            ulElement.innerHTML = `<h1>${elementElement.innerText}</h1>`
        }else {
            const liElement = document.createElement("li")
            liElement.innerText = elementElement.innerText
            ulElement.appendChild(liElement)

        }
    }
}



